package Number;

public class Arith {

	public static void main(String[] args) {
		float a = 45.56f;
		int b = 152;
		System.out.println("a与b的和为: " + (a + b));
		System.out.println("a与b的差为: " + (a - b));
		System.out.println("a与b的积为: " + (a * b));
		System.out.println("a与b的商为: " + (a / b));
		System.out.println("a除以b的余数为: " + (a % b));
		System.out.println("b除以a的余数为: " + (b % a));
		// TODO 整数及小数的四则运算及取余运算

	}

}
