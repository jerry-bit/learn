package Number;

public class Compare {

	public static void main(String[] args) {
		int a = 4;
		int b = 5;
		System.out.println("a<b 的返回值为: " + (a<b));
		System.out.println("a>b 的返回值为: " + (a>b));
		System.out.println("a==b 的返回值为: " + (a==b));
		System.out.println("a<=b 的返回值为: " + (a<=b));
		System.out.println("a>=b 的返回值为: " + (a>=b));
		System.out.println("a!=b 的返回值为: " + (a!=b));
		// TODO 比较运算符输出的是 true 和 false ，为Boolean型

	}

}
